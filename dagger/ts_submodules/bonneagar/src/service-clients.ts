  /**
   * Write service clients to all apps in the monorepo
   */
  @func()
  async writeServiceClients(
    source: Directory,
    appNames?: string[]
  ): Promise<string> {
    const apps = appNames || Object.keys(APP_CONFIGS);
    const results: string[] = [];

    for (const appName of apps) {
      const config = APP_CONFIGS[appName];
      if (!config) {
        results.push(`${appName}: App not found in registry`);
        continue;
      }

      const runtime = config.runtime;
      const clientPath = `${config.path}/src/lib/services`;

      if (runtime === "bun" || runtime === "node") {
        // Write TypeScript client
        const content = this.generateTypescriptClient(appName);
        results.push(`${appName}: Would write TypeScript client to ${clientPath}.ts`);
      } else if (runtime === "uv") {
        // Write Python client
        const content = this.generatePythonClient(appName);
        results.push(`${appName}: Would write Python client to ${clientPath}.py`);
      }
    }

    return results.join("\n");
  }

  /**
   * Get all service endpoints
   */
  @func()
  getServiceEndpoints(local: boolean = false): Record<string, string> {
    const endpoints: Record<string, string> = {};

    for (const [name, service] of Object.entries(SERVICES)) {
      endpoints[name] = local
        ? (service as { local: string }).local
        : (service as { production: string }).production;
    }

    return endpoints;
  }
