export async function graphQuery(
  cypher: string,
  graphName = "curriculum"
): Promise<unknown> {
  const { createClient } = require("redis");

  const client = createClient({
    socket: {
      host: services.vectordb.falkordb.host,
      port: services.vectordb.falkordb.port,
    },
    password: services.vectordb.falkordb.password,
  });

  await client.connect();

  try {
    const result = await client.sendCommand(["GRAPH.QUERY", graphName, cypher]);
    return result;
  } finally {
    await client.disconnect();
  }
}

