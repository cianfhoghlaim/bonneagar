export function createTrace(name: string, metadata?: Record<string, unknown>) {
  // Lazy import to avoid issues when Langfuse SDK not installed
  const { Langfuse } = require("langfuse");

  const client = new Langfuse({
    publicKey: services.langfuse.publicKey,
    secretKey: services.langfuse.secretKey,
    baseUrl: services.langfuse.host,
  });

  return client.trace({
    name,
    metadata: {
      app: "${appName}",
      ...metadata,
    },
  });
}

